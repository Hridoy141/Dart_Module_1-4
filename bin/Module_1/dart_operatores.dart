main(){

  //Assignment operators

  int a = 5;

  a+= 3; // a= a+3
  print(a);

  a-= 2; // a= a-2
  print(a);

  a*= 2; // a= a*2
  print(a);

  //Relational operators

  int x = 10, y = 20;

  print(x<y);
  print(x>y);
  print(x==y);
  print(x!=y);

  //logical operator

  print((x > 5) && (y>20)); //and
  print((x >= 5) && (y>=20)); //and
  print((x > 5) || (y>20)); //or
  print(!(x==y)); //not equal


}